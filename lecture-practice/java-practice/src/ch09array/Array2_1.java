package ch09array;

public class Array2_1 {
    public static void main(String[] args) {

        // 2 * 3  2차원 배열
        int[][] arr = new int[2][3]; //행2, 열3

        //1행
        arr[0][0] = 1;
        arr[0][1] = 2;
        arr[0][2] = 3;

        //2행
        arr[1][2] = 4;
        arr[1][2] = 5;
        arr[1][2] = 6;


    }
}
